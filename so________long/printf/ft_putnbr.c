#include "ft_printf.h"

int	ft_putnbr(int num)
{
	int	x;

	x = 0;
	if (num == -2147483648)
	{
		x = write(1, "-2147483648", 11);
	}
	else
	{
		if (num < 0)
		{
			x += ft_putchar('-');
			num = -num;
		}
		if (num > 9)
		{
			x += ft_putnbr(num / 10);
		}
		x += ft_putchar((num % 10) + 48);
	}
	return (x);
}
