#include "ft_printf.h"

int	putunbr(unsigned int nb)
{
	unsigned int	i;

	i = 0;
	if (nb > 9)
	{
		i += putunbr(nb / 10);
	}
	i += ft_putchar((nb % 10) + 48);
	return (i);
}
